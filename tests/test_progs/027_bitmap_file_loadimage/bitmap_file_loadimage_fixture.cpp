#include <windows.h>
#include "../common/fixture_status.h"

#ifndef IMAGE_BITMAP
#define IMAGE_BITMAP 0
#endif
#ifndef LR_LOADFROMFILE
#define LR_LOADFROMFILE 0x00000010
#endif
#ifndef LR_CREATEDIBSECTION
#define LR_CREATEDIBSECTION 0x00002000
#endif

